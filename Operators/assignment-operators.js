//Assignment Operators

// var1 +=  10  
// var1 -=  10  
// var1 /=  10  
// var1 *=  10  
// var1 %=  10  
// var1 <<=  1  
// var1 >>=  1  
// var1 >>>= 1  

//------------------------------------------------------------

let number = 1234;
console.log(number += 10); //output: 1244

//------------------------------------------------------------

let number1 = 1234;
console.log(number1 -= 10); //output: 1224

//------------------------------------------------------------

let number2 = 1234;
console.log(number2 /= 10); //output: 123.4

//------------------------------------------------------------

let number3 = 1234;
console.log(number3 *= 10); //output: 12340

//------------------------------------------------------------

let number4 = 1234;
console.log(number4 %= 10); //output: 4

//------------------------------------------------------------

let number5 = 1234;
console.log(number5 <<= 10); //output: 1263616

//------------------------------------------------------------

let number6 = 1234;
console.log(number6 >>= 10); //output: 1

//------------------------------------------------------------

let number7 = 1234;
console.log(number7 >>>= 10); //output: 1

